CREATE TABLE IF NOT EXISTS visits (
    projectName String,
    isTor UInt8,
    time DateTime,
    ip String,
    domain String,
    method String,
    path String,
    cookies String,
    params String,
    query String,
    referrer String,
    refererDomain String,
    refId String,
    subId String,
    cookie String,
    sessionId String,
    uuidAction String,
    city String,
    state String,
    continent String,
    country_iso String,
    latitude String,
    longitude String,
    postal_code String,
    userAgent String,
    browser String,
    browserVersion String,
    device String,
    deviceVersion String,
    os String,
    osVersion String,
    level String,
    error String,
    line UInt32,
    file String
    ) ENGINE MergeTree() PARTITION BY toYYYYMM(time) ORDER BY (time)